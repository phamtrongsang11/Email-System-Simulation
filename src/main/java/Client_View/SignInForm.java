    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if (!checkEmpty()) {
            User user = new User();
            user.setEmail(txtEmail.getText() + txtDomain.getText());
            user.setPassword(new String(txtPassword.getPassword()));

            mySocket.sendData(new ObjectWrapper(ObjectWrapper.SIGNIN_USER, user));
        } else {
            JOptionPane.showMessageDialog(this, "Please input all field!!!");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

    private void txtEmailFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtEmailFocusLost
        if (!txtEmail.getText().isEmpty() && !Validation.validationMail(txtEmail.getText() + txtDomain.getText())) {
            JOptionPane.showMessageDialog(this, "This email is invalid");
            txtEmail.setText("");
        }
    }//GEN-LAST:event_txtEmailFocusLost

