    public boolean checkEmpty() {
        if (txtEmail.getText().isEmpty() || new String(txtPassword.getPassword()).isEmpty()) {
            return true;
        }
        return false;
    }

    public void receivedData(ObjectWrapper data) {

        if (!data.getData().equals("false")) {

            if (data.getData().equals("locked")) {
                JOptionPane.showMessageDialog(this, "Your account had been locked for some reason");

            } else if (data.getData().equals("duplicated")) {
                JOptionPane.showMessageDialog(this, "This user had been login in system");
                
            } else {
                JOptionPane.showMessageDialog(this, "Login succesfully!");

                removeOutOfActiveUI();

                User user = (User) data.getData();

                if (user != null) {
                    view.setUser(user);
                    view.enableFunctionLoginSuccess();
                }

                mySocket.sendData(new ObjectWrapper(ObjectWrapper.GET_TOTAL_MAIL_LIST, user));

                this.dispose();
            }

        } else {
            JOptionPane.showMessageDialog(this, "Incorrect username or password!");
        }
    }

    public void removeOutOfActiveUI() {
        ArrayList<ObjectWrapper> activeFunc = mySocket.getActiveUI();
        for (int i = 0; i < activeFunc.size(); i++) {
            if (activeFunc.get(i).getData().equals((Object) this)) {
                activeFunc.remove(i);

            }

        }
    }

    public void customInit() {
        txtDomain.setText("@" + domainName);
    }

